/**
 * Math.pow() 
 * 
 * 函数返回基数（base）的指数（exponent）次幂，即 baseexponent。
 */

console.log(Math.pow(2, 3))
// expected output: 8

console.log(Math.pow(4, 0.5));
// expected output: 2

console.log(Math.pow(7, -2));
// expected output: 0.02040816326530612
//                  (1/49)

console.log(Math.pow(-7, 0.5));
// expected output: NaN